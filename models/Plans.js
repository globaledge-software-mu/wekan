Plans = new Mongo.Collection('plans');

Plans.attachSchema(new SimpleSchema({
	title: { // Unique
    type: String,
  },
  usersQuota: { 
    type: Number,
  },
  boardsQuota: { 
    type: Number,
  },
  pricePerMonth: { 
    type: Number,
  },
  pricePerYear: { 
    type: Number,
  },
  createdAt: {
    type: Date,
    autoValue() { // eslint-disable-line consistent-return
      if (this.isInsert) {
        return new Date();
      } else {
        this.unset();
      }
    },
  },
  modifiedAt: {
    type: Date,
    optional: true,
    autoValue() { // eslint-disable-line consistent-return
      if (this.isUpdate) {
        return new Date();
      } else {
        this.unset();
      }
    },
  },
}));

